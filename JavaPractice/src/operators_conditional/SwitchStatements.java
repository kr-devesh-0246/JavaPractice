package operators_conditional;

public class SwitchStatements {
    public static void main(String[] args) {
        int num = 10;
        switch (num) {
            case 0:
                System.out.println("0");
                break;
            case 10:
                System.out.println("10");
                break;
            case 20:
                System.out.println("20");
                break;
            default:
                System.out.println("None of the above");
        }
    }
}
