package arrays;
import java.util.Arrays;
public class ArrayClassSorting {
    public static void main (String[] args) {
        // initialize and declare an array
        int[] numbers = {1, 2, 3, 4, -6, -5, -4, -3, 0};
        // sorting that array
        Arrays.sort(numbers); // {-6, -5, -4, -3, 0, 1, 2, 3, 4}
    }

}
